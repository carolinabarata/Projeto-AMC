/**
 * Esta classe representa um ponto, ou seja, tem como atributos um valor de tempo (t) e a respetiva ordenada(y).
 */
public class Ponto {
    double t, y;

    /**
     * O método construtor cria um Ponto com os parâmetros t e y que recebe.
     */
    public Ponto(double t, double y) {
        this.t = t;
        this.y = y;
    }
}

